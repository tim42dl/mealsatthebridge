                <p>Our purpose & inspiration comes partly from the classic song “Bridge Over Troubled Water” which speaks of people who are “down & out and on the street ”.  Homelessness is not simply having no place to call home. It is more a lack of connection to family & society in general.
We seek to provide that bridge between the lowest point that a human being can reach & a new life of hope, belonging & purpose. “When friends
just can’t be found & darkness comes” we are there to provide a safe place to meet where they find compassion, acceptance  & a listening, non-judgmental ear.
Our volunteers are aware of the importance of building self-esteem, dignity & a sense of belonging to replace the rejection, abuse & loneliness that
they have experienced.</p>
                <p>A caring community has been established around the basic concept of a shared meal. We provide a “banquet to the poor, the disadvantaged & the outcast” in a side-walk café setting.  We also provide other basic needs such as clothing, blankets & sleeping bags & shelter as well as being a place of referral to other agencies which provide medical, longer term housing & counselling services.</p>
